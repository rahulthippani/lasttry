package model;

public class Emp {
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPosition() {
		return pos;
	}
	public void setPosition(String pos) {
		this.pos = pos;
	}

	int id;
	String name;
	String pos;

	

}
